#include <unistd.h>

int	main(int ac, char **av)
{
	int i;
	int j;
	int flag;

	if (ac == 3)
	{
		flag = 0;
		i = -1;
		j = -1;
		while (av[1][++i] != '\0')
			while (av[2][++j] != '\0')
				if (av[2][j] == av[1][i])
				{
					flag++;
					break ;
				}
		if (i == flag)
			write(1, "1", 1);
		else
			write(1, "0", 1);
	}
	write(1, "\n", 1);
	return (0);
}
