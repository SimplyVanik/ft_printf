#include "libft.h"

void	ft_masdel(char ***s)
{
	char **dub;

	if (s)
	{
		dub = *s;
		if (dub)
		{
			while (*dub)
			{
				ft_strdel(dub);
				dub++;
			}
			free(*s);
			*s = NULL;
		}
	}
}
